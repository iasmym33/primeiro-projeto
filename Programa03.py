num1 = int(input("Digite o primeiro numero: "))
num2 = int(input("Digite o segundo número: "))

Soma = num1 + num2
print(f"A soma de {num1} e {num2} é igual a = {Soma}")

Subtração = num1 - num2
print(f"a subtração de {num1} e {num2} é igual a = {Subtração}")

Multiplicação = num1 * num2
print(f"A multiplicação de {num1} por {num2} é igual a {Multiplicação}")

Divisão = num1 / num2
print(f"A divisáo do {num1} por {num2} é igual a = {Divisão}")

Divisão_inteira = num1 // num2
print(f"A divisão de {num1} por {num2} é igual a = {Divisão_inteira}")

resto_divisao = num1 % num2
print(f"A divisão de {num1} e {num2} resta = {resto_divisao}")

potenciacao = num1 ** num2
print(f"A potenciação de {num1} e {num2} é igual a = {potenciacao}")